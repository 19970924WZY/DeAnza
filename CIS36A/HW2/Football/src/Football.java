public class Football {
	public static void main(String[] args) {
		int length = 360; //pro football fields are 360 feet in length.
		int width = 160; //pro football fields are 160 feet in width.
		int area = length * width; // the area calculation.
		
		System.out.println("The area of a professional football field is " + area +" ft2");
		
		//For changing the area variable, we have to change the length or width value.
		//As the length or width values changed, the result of the calculation for the area will change
		//Thus, the area changed.
		
		/*
		 * Console will print:
		 * The area of a professional football field is 57600 ft2
		 * 
		 */
	}
}
